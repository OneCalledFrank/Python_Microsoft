# Escritura de instrucciones "if"

# Es igual que: a == b
# No es igual: a != b
# menor que: a < b
# mayor que: a > b
# menor o igual que: a <= b
# mayor o igual que: a >= b

# EXPRESIONES DE PRUEBA
a = 97
b = 55
# TEST
if a >= b:
    print(a)


# Ingresa el código
object_size = 10

if object_size > 5:
    print("We need to keep an eye on this object.")
else:
    print("Object poses no threat.")

# Operador AND y OR
a = 23
b = 34
if a == 34 or b == 34:
    print(a + b)

# Mediante el uso de expresiones de prueba, también conocidas como expresiones booleanas, puede ejecutar código de Python de manera condicional. Se usan normalmente en Python para "tomar decisiones" sobre lo que debería ocurrir a continuación mientras se ejecuta un programa. En las unidades anteriores, ha escrito expresiones de prueba como parte de las instrucciones if, else y elif, y ha ajustado aún más las condiciones mediante los operadores and y or.

# Las expresiones booleanas y las instrucciones son una parte clave de la creación de programas de Python complejos, donde quiere que el código solo se ejecute en determinadas condiciones.

# Estos son algunos de los aspectos esenciales que ahora conoce sobre la lógica condicional:

# Los valores booleanos pueden tener uno de dos valores, True o False.
# Las instrucciones if, elsey elif se pueden usar para ejecutar código bajo condiciones específicas.
# Los and y or operadores y se pueden usar para combinar expresiones de prueba y crear condiciones más complejas.
